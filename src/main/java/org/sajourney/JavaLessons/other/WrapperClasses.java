package org.sajourney.JavaLessons.other;

public class WrapperClasses {
    public static void main(String[] args){
        //primitive data types to reference data types
        //autoboxing = automatic conversion primitive to object wrapper class
        // unboxing = reverse of autoboxing (automatic)

        //autoboxing
        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double e = 3.14;

        //unboxing
        if (a == true){
            System.out.println("true");
        }



    }
}
